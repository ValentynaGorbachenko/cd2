/*
Setting and Swapping
*/
let myName = 'Valentyna';
let myNumber = 8;
var x;
console.log(typeof myNumber);
console.log(typeof myName);
console.log(typeof x);
console.log(myName, myNumber);
x = myNumber;
myNumber = myName;
myName = x;
console.log(myName + myNumber);
console.log(typeof myNumber);
console.log(typeof myName, myNumber);